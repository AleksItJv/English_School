public class Students {
    private int id;
    private int idGroup;
    private String fname;
    private String sname;
    private String lname;
    private String phone;

    public Students(int id, int idGroup, String fname, String sname, String lname, String phone) {
        this.id = id;
        this.idGroup = idGroup;
        this.fname = fname;
        this.sname = sname;
        this.lname = lname;
        this.phone = phone;
    }

}
